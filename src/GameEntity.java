public class GameEntity {

    private String hero;
    private int heroHp;
    private int heroDmg;
    //-----------------------
    private String boss;
    private int bossHp;
    private int bossDmg;
    //-----------------------


    public String getHero() {
        return hero;
    }

    public void setHero(String hero) {
        this.hero = hero;
    }
    //-----------------------
    public int getHeroHp() {
        return heroHp;
    }

    public void setHeroHp(int heroHp) {
        this.heroHp = heroHp;
    }
    //-----------------------
    public int getHeroDmg() {
        return heroDmg;
    }

    public void setHeroDmg(int heroDmg) {
        this.heroDmg = heroDmg;
    }
    //-----------------------
    public String getBoss() {
        return boss;
    }

    public void setBoss(String boss) {
        this.boss = boss;
    }
    //-----------------------
    public int getBossHp() {
        return bossHp;
    }

    public void setBossHp(int bossHp) {
        this.bossHp = bossHp;
    }
    //-----------------------
    public int getBossDmg() {
        return bossDmg;
    }

    public void setBossDmg(int bossDmg) {
        this.bossDmg = bossDmg;
    }

}
